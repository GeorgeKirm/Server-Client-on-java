    private void sentBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sentBActionPerformed
        if (sentToT.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No sent andress.",
                    "Error message", JOptionPane.PLAIN_MESSAGE);
        } else if (sentToT.getText().equals(userNameS)) {
            JOptionPane.showMessageDialog(null, "You cant sent email to yourself.",
                    "Error message", JOptionPane.PLAIN_MESSAGE);
        } else {//boolean isNew, String senter, String reciever, String subject, String mainBody, int howManyEmails
            Email mail = new Email(true, userNameS, sentToT.getText(), subjectT.getText(), mainBodyT.getText());
            if (client.sentMessage(mail)) {
                dataLE.add(mail);
                dataNamesLS.add(mail.subjectGetter());
                lastValueSelectedI = jList2.getSize().height;
                emailList();
                clearer();
                jTabbedPane1.setSelectedIndex(1);
                JOptionPane.showMessageDialog(null, "The message sented successfully.",
                        "Info-chan message", JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to sent the message, plz try again.",
                        "Error message", JOptionPane.PLAIN_MESSAGE);
            }
        }
    }//GEN-LAST:event_sentBActionPerformed

