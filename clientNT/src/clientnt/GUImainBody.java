    private void emailList() {
        DefaultListModel listModel = new DefaultListModel();
        for (String s : dataNamesLS) {
            listModel.addElement(s);
        }
        jList2.setModel(listModel);
        if (lastValueSelectedI == -1) {
            if (!jList2.isSelectionEmpty()) {
                jList2.setSelectedIndex(0);
            }
        } else {
            jList2.setSelectedIndex(lastValueSelectedI);
        }
//        jPanel2.repaint();
//        jPanel2.revalidate();
    }

    private void emailList(List<Email> dataAll) {
        Object selectedValue = jList2.getSelectedValue();
        if (selectedValue != null) {
            lastValueSelectedI = dataNamesLS.indexOf(selectedValue.toString());
        }
//        System.out.println("edw");
//        client= new ClientNT();
//        this.dataLE = new ArrayList<>();
        this.dataLE = dataAll;
//        for (Email loula : dataLE) {
//            System.out.println(loula.soutAllGetter());
//        }
        this.dataNamesLS = new ArrayList<>();
        for (int i = 0; dataAll.size() - 1 >= i; i++) {
            if (dataAll.get(i).isNewBGetter() == true) {
                this.dataNamesLS.add("->" + dataAll.get(i).subjectGetter());
            } else {
                this.dataNamesLS.add("  " + dataAll.get(i).subjectGetter());
            }
        }
        jList2.removeAll();
        emailList();
    }

